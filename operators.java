public class operators {
    public static void main(String[] args[]) {
        int a = 2;
        int b = 3;

        System.out.println(10%1);
        System.out.println(10/1);
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(2>3);
        System.out.println(2!=3);
        System.out.println(a*b);
    }
}
